    private void buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonActionPerformed
        // SAVE
        String NAME = name.getText();
        String IP = ip.getText();
        String PORT = port.getText();
        String PATH = path.getText();
        String FILE = file.getText();
        String HKCU = hkcu.getText();
        String HKLM = hklm.getText();
        
        Profile profile = new Profile(NAME, IP, PORT, PATH, FILE, HKCU, HKLM);
        
        try{
            File file = new File("profiles.dat");
            FileWriter fileWriter = new FileWriter(file);
            PrintWriter printWriter = new PrintWriter(file);
            
            printWriter.write(profile.getString()+"\n");
            
            printWriter.close();
            fileWriter.close();
        } catch (FileNotFoundException ex) {
            Logger.getLogger(EditProfile.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(EditProfile.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        MAIN.UpdateList();
        dispose();
    }//GEN-LAST:event_buttonActionPerformed

